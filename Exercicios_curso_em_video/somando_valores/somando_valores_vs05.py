# Somando valores versão 04:

n1=input('Digite um valor: ').isnumeric()  # retorno se for string será falso, se for inteiro: True
n2=input('Digite outro valor: ').isnumeric() # Retorno se for Str será False, se for INT será True
soma =  int(n1)+int(n2)  # Podemos colocar o tipo de dado aqui ou dentro do print conforme a versão 03.
print('A soma entre',n1,'e',n2,'é igual a',soma,'!')
